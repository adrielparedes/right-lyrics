package io.veicot.rightlyrics.resource;

public class Response<T> {

    public T data;
    public Integer status;

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }
}
